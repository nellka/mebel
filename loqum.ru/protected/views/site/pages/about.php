<h1>Спонсорство. Частный взгляд.</h1>
<p>Спонсорство -  это долговременные сексуальные отношения состоятельного мужчины (спонсора) и  девушки, нуждающейся в материальной поддержке (содержанки).</p>
<p>Такие  отношения считаю ближе к обычным, &laquo;безденежным&raquo;, отношениям, чем к проституции.  Большая часть содержанок никогда проститутками не работали и категорически не  планируют. При этом предполагается дистанция между спонсором и содержанкой, например,  невмешательство в личную жизнь спонсора, и стабильное&nbsp; финансовое содержание девушки.</p>
<h2>Когда мужчина  решает найти содержанку?</h2>
<p>Большинство  мужчин решают найти содержанку в нескольких случаях:</p>
<ul>
    <li>как  любовницу (если женаты), без претензий на роль жены и более спокойный вариант  чем обычная любовница. Более предсказуемо и спокойно.</li>
    <li>как замену  девушке. Расстался, новые отношения начинать не хочет, а один быть не может или  не привык.</li>
    <li> если много  поездок / командировок и на обычные постоянные отношения нет ни времени, ни  возможности. А так &ndash; стабильный, надежный вариант.</li>
</ul>
<h2>Когда девушка  ищет спонсора?</h2>
<ul>
    <li> поиск любовника-спонсора.  Аналог мужского &laquo;расстался&raquo;. Т.е. рассталась, новые отношения заводить не  хочется, некоторое недоверие, еще и материальные трудности. А почему бы не  завести успешного любовника постарше? Внимание, секс, поддержка, при этом можно  соблюдать дистанцию в отношениях и это будет нормально. Почему нет?</li>
    <li> нужны  деньги, большая сумма, других способов нет. </li>
    <li> желание  красивой жизни?</li>
    <li> ??? <br />
        &nbsp;&nbsp; <i>автор &ndash; мужчина, поэтому не полностью  понимает мотивы. </i></li>
</ul>
<p>Сами  отношения обычно состоят или из встреч (наиболее частый вариант) несколько раз  в неделю, или совместное проживание (довольно редкий вариант).</p>
