<p>Вопреки  распространенному мнению, чтобы позволить себе содержанку миллионы не нужны.  Если вы нормальный мужик, и у вас есть лишние 50 000 рублей (пятьдесят тысяч) &ndash;  этого может быть достаточно.</p>
<p> Обычно,  девушек устраивает содержание в размере от 35000 до 100000 в месяц. Не бойтесь  предлагать! Если вам ответят что слишком мало &ndash; не факт что девушка вообще  кого-то ищет. Реальные содержанки вполне реально оценивают ситуацию. </p>
<p>Чтобы было  понятнее, для сравнения из смежного&hellip; </p>
<ol start="1" type="1">
    <li>Проститутки имеют чистыми на руки       примерно 60-70000 в месяц. Их в Москве даже по &laquo;Русдосугу&raquo; на данный       момент эдак 3-4 тысячи. </li>
    <li>Средняя зарплата секретаря с       интимом &ndash; 70-80000 в месяц. При этом надо ходить на работу, к тому же быть       в окружении людей, которые в курсе &laquo;спецобязанностей&raquo;.</li>
    <li>Как правило, содержанки имеют       постоянную работу, которая приносит меньше. Девушка, зарабатывающая 80000       в месяц, вряд ли будет искать спонсора. <br />
        Ездить на пятидневку за 30000 и при этом просить &laquo;от 150&raquo; - странно,       согласитесь.</li>
</ol>
<p>Как видите,  нигде даже цифры &laquo;100&raquo; нет.</p>
