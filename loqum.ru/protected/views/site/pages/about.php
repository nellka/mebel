<p>  Например! Вы  договорились на 60000 в месяц, 2-3 встречи в неделю. Это получается 10-11  встреч в месяц, или 5400 за встречу.<br />
    Соответственно,  для первой пробной встречи цена в 6000-8000 будет адекватной. </p>
